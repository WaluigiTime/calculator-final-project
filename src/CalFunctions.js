import React from "react";

export function addFunc(x, y) {
  return x + y;
}

export function multiFunc(x, y) {
  return x * y;
}
export function subtractFunc(x, y) {
  return x - y;
}

export function divideFunc(x, y) {
  return x / y;
}

export function modFunc(x, y) {
  return x % y;
}

export function sqrrootFunc(x, y) {
  return Math.sqrt(x);
}

export function powFunc(x, y) {
  return Math.pow(x, y);
}
